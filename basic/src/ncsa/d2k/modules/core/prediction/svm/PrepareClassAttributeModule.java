package ncsa.d2k.modules.core.prediction.svm;

import ncsa.d2k.modules.core.datatype.table.ExampleTable;


/**
 * <p>Title:</p>
 *
 * <p>Description: this module will prepare the output feature of an
 * ExampleTable for the use of an Isvm Module and the likes. The output Table of
 * this module will have only 1 or -1 in the output feature. it replaces the
 * zeros with ones.</p>
 *
 * <p>Copyright: Copyright (c) 2004</p>
 *
 * <p>Company:</p>
 *
 * @author  vered goren
 * @version 1.0
 */
public class PrepareClassAttributeModule
   extends ncsa.d2k.core.modules.DataPrepModule {

   //~ Methods *****************************************************************

   /**
    * Performs the main work of the module.
    *
    * @throws Exception if a problem occurs while performing the work of the
    *                   module
    */
   protected void doit() throws Exception {
      ExampleTable et = (ExampleTable) pullInput(0);
      int[] outIdx = et.getOutputFeatures();

      for (int i = 0; i < et.getNumRows(); i++) {

         for (int j = 0; j < outIdx.length; j++) {

            if (et.getInt(i, outIdx[j]) == 0) {
               et.setInt(-1, i, outIdx[j]);
            }
         }
      }

      pushOutput(et, 0);
   }


   /**
    * Returns a description of the input at the specified index.
    *
    * @param  index Index of the input for which a description should be
    *               returned.
    *
    * @return <code>String</code> describing the input at the specified index.
    */
   public String getInputInfo(int index) {

      switch (index) {

         case 0:
            return "An ExampleTable with scalar binary output feature (that contain only zero and one).";

         default:
            return "No such input";
      }
   }


   /**
    * Returns the name of the input at the specified index.
    *
    * @param  index Index of the input for which a name should be returned.
    *
    * @return <code>String</code> containing the name of the input at the
    *         specified index.
    */
   public String getInputName(int index) {

      switch (index) {

         case 0:
            return "Input Table";

         default:
            return "No such input";
      }
   }


   /**
    * Returns an array of <code>String</code> objects each containing the fully
    * qualified Java data type of the input at the corresponding index.
    *
    * @return An array of <code>String</code> objects each containing the fully
    *         qualified Java data type of the input at the corresponding index.
    */
   public String[] getInputTypes() {
      String[] types = { "ncsa.d2k.modules.core.datatype.table.ExampleTable" };

      return types;
   }


   /**
    * Describes the purpose of the module.
    *
    * @return <code>String</code> describing the purpose of the module.
    */
   public String getModuleInfo() {
      return "<p>Overview:" +
             "      This module prepares the output features of the <i>Input Table</i> " +
             "      for the use of SVM modules and the likes." +
             "    </p>" +
             "    <p>" +
             "      The output features should be scalar and binary (1 and 0)," +
             "      the 0 values are then replaced with -1." +
             "    </p>" +
             "    <p>" +
             "      This module changes the content of the <i>Input Table</i>." +
             "    </p>";
   }


   /**
    * Returns the name of the module that is appropriate for end-user
    * consumption.
    *
    * @return The name of the module.
    */
   public String getModuleName() { return "Prepare Output Attribute For SVM"; }


   /**
    * Returns a description of the output at the specified index.
    *
    * @param  index Index of the output for which a description should be
    *               returned.
    *
    * @return <code>String</code> describing the output at the specified index.
    */
   public String getOutputInfo(int index) {

      switch (index) {

         case 0:
            return "Same as the input table with transformed output features - all zeros are replaced with -1.";

         default:
            return "No such output";
      }
   }


   /**
    * Returns the name of the output at the specified index.
    *
    * @param  index Index of the output for which a description should be
    *               returned.
    *
    * @return <code>String</code> containing the name of the output at the
    *         specified index.
    */
   public String getOutputName(int index) {

      switch (index) {

         case 0:
            return "Output Table";

         default:
            return "No such output";
      }
   }


   /**
    * Returns an array of <code>String</code> objects each containing the fully
    * qualified Java data type of the output at the corresponding index.
    *
    * @return An array of <code>String</code> objects each containing the fully
    *         qualified Java data type of the output at the corresponding index.
    */
   public String[] getOutputTypes() {
      String[] types = { "ncsa.d2k.modules.core.datatype.table.ExampleTable" };

      return types;
   }
} // end class PrepareClassAttributeModule
